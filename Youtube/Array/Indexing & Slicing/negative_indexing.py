import numpy as np

# It is used to pull out the element from last of the array and it starts from -1

array = np.array([1,2,3,4,5])
print(f"Last Element = {array[-1]}")
print(f"2nd Last Element = {array[-2]} ")
print(f"3rd Last Element = {array[-3]}")
print(f"4th Last Element = {array[-4]}")
print(f"5th Last Element = {array[-5]}")