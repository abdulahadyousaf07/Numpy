import numpy as np

#It applies operator to all elements automatically you do not need to apply for loop and then applying on a single element separately

array = np.array([1,2,3,4,5])
print(array+2)  #Addition
print(array-2)  #Subtraction
print(array*2)  #Multiplication
print(array/2)  #Division
print(array%2)  #Modulus Operator
print(array**2) #Exponential Operator
print(array//2) #Floor Division Operator